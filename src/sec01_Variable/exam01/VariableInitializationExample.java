package sec01_Variable.exam01;

public class VariableInitializationExample {
    public static void main(String[] args) {
        // 변수 선언과 동시에 20이라는 정수 리터럴 초기화
        int value = 20;

        int result = value + 10;

        System.out.println("result = " + result);
    }

    /*
        result = 30
    */
}
